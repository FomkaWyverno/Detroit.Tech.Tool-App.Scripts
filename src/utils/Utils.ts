
/**
 * Створює проміс, який виконується після затримки, заданої в параметрі.
 * Використовується для затримки виконання наступного кроку в асинхронному процесі.
 * 
 * @param ms Час затримки у мілісекундах, після якого проміс буде виконаний.
 * @returns Порожній проміс для ассинхроного очікування.
 */
export function delay(ms: number): Promise<void> {
    return new Promise<void>(resolve => window.setTimeout(resolve, ms));
}